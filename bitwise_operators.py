a = 5  # 0b0101
b = 3  # 0b0011

print("Bitwise AND:", a & b)   # 0b0001 -> 1
print("Bitwise OR:", a | b)    # 0b0111 -> 7
print("Bitwise XOR:", a ^ b)   # 0b0110 -> 6
print("Bitwise NOT of a:", ~a) # -6 (Two's complement)
print("Bitwise left shift:", a << 1)  # 0b1010 -> 10
print("Bitwise right shift:", a >> 1) # 0b0010 -> 2
